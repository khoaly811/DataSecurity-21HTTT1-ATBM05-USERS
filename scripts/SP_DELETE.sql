CREATE OR REPLACE PROCEDURE SP_DELETE_NHANSU(
    
    INP_MANV IN CHAR
)AUTHID CURRENT_USER AS
BEGIN
    DELETE FROM C##QLK.NHANSU ns WHERE ns.MANV = INP_MANV;
    COMMIT;
END;
