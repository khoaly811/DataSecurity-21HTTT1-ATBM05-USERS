CREATE OR REPLACE PROCEDURE SP_DELETE_PHANCONG(
    
    INP_TENGV IN VARCHAR2,
    INP_TENHP IN VARCHAR2,
    INP_HK IN NUMBER,
    INP_NAM IN NUMBER,
    INP_MACT IN VARCHAR2
)AUTHID CURRENT_USER AS
    V_MAGV VARCHAR2(20);
    V_MAHP INT;
BEGIN
    SELECT MANV INTO V_MAGV FROM C##QLK.NHANSU WHERE HOTEN=INP_TENGV;
    SELECT MAHP INTO V_MAHP FROM C##QLK.HOCPHAN WHERE TENHP=INP_TENHP;
    DELETE FROM C##QLK.PHANCONG pc WHERE pc.MAGV = V_MAGV and
    pc.MAHP = V_MAHP and
    pc.HK = INP_HK and
    pc.NAM = INP_NAM and
    pc.MACT = INP_MACT;

END;
/
SELECT HOTEN, TENHP, HK, NAM, MACT, PC.MAHP, MAGV
        FROM C##QLK.PHANCONG PC JOIN C##QLK.NHANSU NS ON PC.MAGV = NS.MANV
        JOIN C##QLK.HOCPHAN HP ON HP.MAHP = PC.MAHP;

BEGIN
    SP_GRANT_EXECUTE('SP_DELETE_PHANCONG', 'TKHOA');
    SP_GRANT_EXECUTE('SP_DELETE_PHANCONG', 'TDV');
    SP_GRANT_EXECUTE('SP_DELETE_PHANCONG', 'GV');
    SP_GRANT_EXECUTE('SP_DELETE_PHANCONG', 'GVU');
    SP_GRANT_EXECUTE('SP_DELETE_PHANCONG', 'NVCB');
    SP_GRANT_EXECUTE('SP_DELETE_PHANCONG', 'SV');
END;
