CREATE OR REPLACE PROCEDURE SP_DELETE_DANGKY(
    INP_TENSV IN VARCHAR2,
    INP_TENGV IN VARCHAR2,
    INP_TENHP IN VARCHAR2,
    INP_HK IN NUMBER,
    INP_NAM IN NUMBER,
    INP_MACT IN VARCHAR2
)AUTHID CURRENT_USER AS
    V_MASV VARCHAR2(20);
    V_MAGV VARCHAR2(20);
    V_MAHP INT;
BEGIN
    SELECT MASV INTO V_MASV FROM C##QLK.SINHVIEN SV WHERE SV.HOTEN=INP_TENSV;
    SELECT MANV INTO V_MAGV FROM C##QLK.NHANSU NS WHERE NS.HOTEN=INP_TENGV;
    SELECT MAHP INTO V_MAHP FROM C##QLK.HOCPHAN WHERE TENHP = INP_TENHP;
    DELETE FROM C##QLK.DANGKY WHERE 
    MASV = V_MASV AND
    MAGV = V_MAGV AND
    MAHP = V_MAHP AND
    HK = INP_HK AND
    NAM = INP_NAM AND
    MACT = INP_MACT;
END;
